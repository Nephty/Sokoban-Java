package model;

/**
 * The <code>Difficulty enum</code> is an enum to know the difficulty of the map we're currently
 * playing on. There's 4 types of difficulties :
 * {@link #EASY}
 * {@link #NORMAL}
 * {@link #HARD}
 * {@link #EXTREME}
 */
public enum Difficulty {
    /**
     * EASY Difficulty
     */
    EASY,

    /**
     * NORMAL Difficulty
     */
    NORMAL,

    /**
     * HARD Difficulty
     */
    HARD,

    /**
     * EXTREME Difficulty
     */
    EXTREME
}
